public class Q42Math {
    public static void main(String[] args) {
        int num1 = 27;
        int num2 = -45;
        double num3 = 84.6;
        double num4 = 0.45;

        System.out.println("Minimum of '" + num1 + "' and '" + num2 + "' = " + Math.min(num1, num2));
        System.out.println("Maximum of '" + num1 + "' and '" + num2 + "' = " + Math.max(num1, num2));
        System.out.println("Minimum of '" + num3 + "' and '" + num4 + "' = " + Math.min(num3, num4));
        System.out.println("Maximum of '" + num3 + "' and '" + num4 + "' = " + Math.max(num4, num4));
    }
}
